const root = document.documentElement;
const header = document.querySelector('#header');
const menuToggle = document.querySelector('.menu-toggle');
const languageSwitch = document.querySelector('#language-switch');
const nav = document.querySelector('#nav');
const navLinks = [...document.querySelectorAll('.site-nav > a:not(.mobile-nav-cta)')];
const sections = [...document.querySelectorAll('main section[id]')];
const backTop = document.querySelector('.back-top');
const contactForm = document.querySelector('.contact-form');
const formNote = document.querySelector('#form-note');
const heroGallery = document.querySelector('.hero-gallery');
const previewCards = [...document.querySelectorAll('.preview-card')];
const reduceMotion = window.matchMedia('(prefers-reduced-motion: reduce)');
let currentLanguage = getSavedLanguage();

function getSavedLanguage() {
  try { return localStorage.getItem('ahmad-language') === 'ar' ? 'ar' : 'en'; }
  catch { return 'en'; }
}

function saveLanguage(language) {
  try { localStorage.setItem('ahmad-language', language); }
  catch { /* Storage can be unavailable on restricted file URLs. */ }
}

function closeMenu() {
  menuToggle.classList.remove('is-active');
  nav.classList.remove('is-open');
  document.body.classList.remove('menu-open');
  menuToggle.setAttribute('aria-expanded', 'false');
  menuToggle.setAttribute('aria-label', translations[currentLanguage].openNav);
}

function applyLanguage(language) {
  const copy = translations[language];
  currentLanguage = language;
  root.lang = language;
  root.dir = language === 'ar' ? 'rtl' : 'ltr';
  document.title = copy.pageTitle;
  document.querySelector('meta[name="description"]').content = copy.pageDescription;

  document.querySelectorAll('[data-i18n]').forEach((element) => {
    const value = copy[element.dataset.i18n];
    if (value !== undefined) element.textContent = value;
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach((element) => {
    const value = copy[element.dataset.i18nPlaceholder];
    if (value !== undefined) element.placeholder = value;
  });
  document.querySelectorAll('[data-i18n-aria]').forEach((element) => {
    const value = copy[element.dataset.i18nAria];
    if (value !== undefined) element.setAttribute('aria-label', value);
  });

  languageSwitch.setAttribute('aria-label', language === 'en' ? 'Switch to Arabic' : 'التبديل إلى الإنجليزية');
  saveLanguage(language);
}

applyLanguage(currentLanguage);

languageSwitch.addEventListener('click', () => {
  document.body.classList.add('language-changing');
  closeMenu();
  window.setTimeout(() => {
    applyLanguage(currentLanguage === 'en' ? 'ar' : 'en');
    document.body.classList.remove('language-changing');
  }, 120);
});

function updateScrollState() {
  header.classList.toggle('scrolled', window.scrollY > 16);
  backTop.classList.toggle('visible', window.scrollY > 650);
}

updateScrollState();
window.addEventListener('scroll', updateScrollState, { passive: true });

menuToggle.addEventListener('click', () => {
  const open = menuToggle.classList.toggle('is-active');
  nav.classList.toggle('is-open', open);
  document.body.classList.toggle('menu-open', open);
  menuToggle.setAttribute('aria-expanded', String(open));
  menuToggle.setAttribute('aria-label', translations[currentLanguage][open ? 'closeNav' : 'openNav']);
});

nav.querySelectorAll('a').forEach((link) => link.addEventListener('click', closeMenu));
document.addEventListener('click', (event) => {
  if (!nav.classList.contains('is-open') || nav.contains(event.target) || menuToggle.contains(event.target)) return;
  closeMenu();
});
document.addEventListener('keydown', (event) => {
  if (event.key !== 'Escape' || !nav.classList.contains('is-open')) return;
  closeMenu();
  menuToggle.focus();
});
window.addEventListener('resize', () => { if (window.innerWidth > 768) closeMenu(); });

if ('IntersectionObserver' in window && !reduceMotion.matches) {
  const revealObserver = new IntersectionObserver((entries, observer) => {
    entries.forEach((entry) => {
      if (!entry.isIntersecting) return;
      entry.target.classList.add('visible');
      observer.unobserve(entry.target);
    });
  }, { threshold: 0.1, rootMargin: '0px 0px -32px' });

  document.querySelectorAll('.reveal').forEach((element) => revealObserver.observe(element));
} else {
  document.querySelectorAll('.reveal').forEach((element) => element.classList.add('visible'));
}

if ('IntersectionObserver' in window) {
  const activeObserver = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (!entry.isIntersecting) return;
      navLinks.forEach((link) => link.classList.toggle('active', link.hash === `#${entry.target.id}`));
    });
  }, { rootMargin: '-32% 0px -58% 0px' });

  sections.forEach((section) => activeObserver.observe(section));
}

document.querySelectorAll('.ripple').forEach((button) => {
  button.addEventListener('click', (event) => {
    if (reduceMotion.matches) return;
    const rect = button.getBoundingClientRect();
    const size = Math.max(rect.width, rect.height);
    const wave = document.createElement('span');
    wave.className = 'ripple-wave';
    wave.style.width = wave.style.height = `${size}px`;
    wave.style.left = `${event.clientX - rect.left - size / 2}px`;
    wave.style.top = `${event.clientY - rect.top - size / 2}px`;
    button.appendChild(wave);
    wave.addEventListener('animationend', () => wave.remove());
  });
});

if (heroGallery && !reduceMotion.matches && window.matchMedia('(pointer: fine)').matches) {
  heroGallery.addEventListener('pointermove', (event) => {
    const rect = heroGallery.getBoundingClientRect();
    const x = (event.clientX - rect.left) / rect.width - 0.5;
    const y = (event.clientY - rect.top) / rect.height - 0.5;
    previewCards.forEach((card) => {
      const depth = Number(card.dataset.depth || 1);
      card.style.setProperty('--parallax-x', `${x * 13 * depth}px`);
      card.style.setProperty('--parallax-y', `${y * 10 * depth}px`);
    });
  });

  heroGallery.addEventListener('pointerleave', () => {
    previewCards.forEach((card) => {
      card.style.setProperty('--parallax-x', '0px');
      card.style.setProperty('--parallax-y', '0px');
    });
  });
}

if (contactForm) {
  contactForm.addEventListener('submit', () => {
    const submitButton = contactForm.querySelector('.form-submit');
    submitButton.disabled = true;
    submitButton.querySelector('span').textContent = translations[currentLanguage].sendingMessage;
    formNote.classList.add('success');
    formNote.querySelector('span').textContent = translations[currentLanguage].formSending;
  });
}

backTop.addEventListener('click', () => window.scrollTo({ top: 0, behavior: reduceMotion.matches ? 'auto' : 'smooth' }));
document.querySelector('#year').textContent = new Date().getFullYear();
